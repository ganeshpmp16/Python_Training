import math # importing the built-in math module
# using the math module
print("Square root of 16:", math.sqrt(16))  # prints 4.0
print("Sine of 90 degrees:", math.sin(math.radians(90)))  # prints 1.0
print("Cosine of 0 degrees:", math.cos(math.radians(0)))  # prints 1.0
print("Pi:", math.pi)  # prints 3.141592653589793
